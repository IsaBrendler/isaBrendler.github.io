function setMedia(el, src) {
  if (!el) return;
  if (src) {
    const img = document.createElement("img");
    img.src = src;
    img.alt = "";
    el.appendChild(img);
  }
}

function renderProject(id) {
  const project = PROJECTS[id];
  if (!project) {
    document.getElementById("project-title").textContent = "Project not found";
    return;
  }

  document.title = `${project.title} — Isadora Brendler`;

  document.getElementById("project-title").textContent = project.title;
  document.getElementById("project-subtitle").textContent = project.subtitle || "";

  document.getElementById("section1-title").textContent = project.section1.title;
  document.getElementById("section1-body").textContent = project.section1.body;

  const section2Col = document.getElementById("section2-col");
  if (section2Col) {
    if (project.section3 && project.section3.body) {
      document.getElementById("section2-title").textContent = project.section2?.title || "Details";
      document.getElementById("section3-body").textContent = project.section3.body;
      section2Col.hidden = false;
    } else {
      section2Col.hidden = true;
    }
  }

  setMedia(document.getElementById("split-image"), project.image);

  if (project.link) {
    const link = document.getElementById("project-link");
    link.href = project.link;
    link.textContent = project.linkLabel || "View project";
    link.hidden = false;
  }

  // Render Metadata (Activities & Skills)
  const metadataSection = document.getElementById("project-metadata");
  const activitiesBlock = document.getElementById("project-metadata-activities-block");
  const activitiesList = document.getElementById("project-metadata-activities");
  const skillsBlock = document.getElementById("project-metadata-skills-block");
  const skillsContainer = document.getElementById("project-metadata-skills");

  let hasMetadata = false;

  if (metadataSection && activitiesBlock && activitiesList && skillsBlock && skillsContainer) {
    // Render activities
    if (project.activities && project.activities.length > 0) {
      activitiesList.innerHTML = "";
      project.activities.forEach((act) => {
        const li = document.createElement("li");
        li.innerHTML = act;
        activitiesList.appendChild(li);
      });
      activitiesBlock.hidden = false;
      hasMetadata = true;
    } else {
      activitiesBlock.hidden = true;
    }

    // Render skills grouped by category
    if (project.skills && Object.keys(project.skills).length > 0) {
      skillsContainer.innerHTML = "";
      let hasSkills = false;

      Object.entries(project.skills).forEach(([category, list]) => {
        if (list && list.length > 0) {
          hasSkills = true;
          const catDiv = document.createElement("div");
          catDiv.className = "project-metadata__skill-category";

          const catName = document.createElement("div");
          catName.className = "project-metadata__category-name";
          catName.textContent = category;
          catDiv.appendChild(catName);

          const tagsDiv = document.createElement("div");
          tagsDiv.className = "project-metadata__tags";

          list.forEach((skill) => {
            const tag = document.createElement("span");
            tag.className = "project-metadata__tag";
            tag.textContent = skill;
            tagsDiv.appendChild(tag);
          });

          catDiv.appendChild(tagsDiv);
          skillsContainer.appendChild(catDiv);
        }
      });

      if (hasSkills) {
        skillsBlock.hidden = false;
        hasMetadata = true;
      } else {
        skillsBlock.hidden = true;
      }
    } else {
      skillsBlock.hidden = true;
    }

    if (hasMetadata) {
      metadataSection.hidden = false;
    } else {
      metadataSection.hidden = true;
    }
  }

  // Set project-specific body class to allow layout customizations
  document.body.className = `project-body project-body--${id}`;

  // Gather screenshots
  const screenshots = [];
  if (project.media) {
    if (project.media.wide) screenshots.push(project.media.wide);
    if (project.media.video) screenshots.push(project.media.video);
    if (project.media.small) screenshots.push(project.media.small);
    if (project.media.landscape) screenshots.push(project.media.landscape);
    if (project.media.tall) screenshots.push(project.media.tall);
    if (project.media.gallery) {
      project.media.gallery.forEach(src => {
        if (!screenshots.includes(src)) screenshots.push(src);
      });
    }
  }

  // Showcase elements (interleave first screenshots with columns)
  const showcase1El = document.getElementById("showcase-img-1");
  const showcase2El = document.getElementById("showcase-img-2");

  if (showcase1El) {
    showcase1El.innerHTML = "";
    showcase1El.hidden = true;
  }
  if (showcase2El) {
    showcase2El.innerHTML = "";
    showcase2El.hidden = true;
  }

  let galleryImages = [];

  if (screenshots.length > 0) {
    if (showcase1El) {
      setMedia(showcase1El, screenshots[0]);
      showcase1El.hidden = false;
    }

    if (section2Col && !section2Col.hidden && screenshots.length > 1) {
      if (showcase2El) {
        setMedia(showcase2El, screenshots[1]);
        showcase2El.hidden = false;
      }
      galleryImages = screenshots.slice(2);
    } else {
      galleryImages = screenshots.slice(1);
    }
  }

  // Render remaining screenshots gallery
  const galleryEl = document.getElementById("project-gallery");
  if (galleryEl) {
    if (galleryImages.length > 0) {
      galleryEl.innerHTML = "";
      galleryImages.forEach((src) => {
        const item = document.createElement("div");
        item.className = "project-template__gallery-item";
        const img = document.createElement("img");
        img.src = src;
        img.alt = "";
        item.appendChild(img);
        galleryEl.appendChild(item);
      });
      galleryEl.hidden = false;
    } else {
      galleryEl.hidden = true;
    }
  }
}

// Lightbox Modal Script
const lightbox = document.getElementById("lightbox");
const lightboxImg = document.getElementById("lightbox-img");
const lightboxClose = document.getElementById("lightbox-close");

function openLightbox(src) {
  if (!lightbox || !lightboxImg) return;
  lightboxImg.src = src;
  lightbox.setAttribute("aria-hidden", "false");
}

lightboxClose?.addEventListener("click", () => {
  lightbox?.setAttribute("aria-hidden", "true");
});

lightbox?.addEventListener("click", (e) => {
  if (e.target === lightbox || e.target === lightboxImg) {
    lightbox.setAttribute("aria-hidden", "true");
  }
});

document.addEventListener("click", (e) => {
  const isGalleryImg = e.target.tagName === "IMG" && e.target.closest(".project-template__gallery-item");
  const isShowcaseImg = e.target.tagName === "IMG" && e.target.closest(".project-template__showcase-media");
  const isHeaderImg = e.target.tagName === "IMG" && e.target.closest("#split-image");

  if (isGalleryImg || isShowcaseImg || isHeaderImg) {
    openLightbox(e.target.src);
  }
});

const projectId = new URLSearchParams(location.search).get("id");
renderProject(projectId);
